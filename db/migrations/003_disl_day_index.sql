-- Индекс по дате справки для быстрой очистки при почасовом импорте.
-- При каждом импорте удаляются все строки за тот же день + тип_справки,
-- поэтому в таблице хранится только последняя справка за каждый день.
-- =============================================================================

CREATE INDEX ix_disl_rjd_report_dt
    ON xx_dislocation_rjd (TRUNC(report_dt), type_reference);
